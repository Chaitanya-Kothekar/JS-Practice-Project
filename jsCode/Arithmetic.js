// Addition (+)
let sum = 5 + 3;  
console.log("Sum:", sum);  // Sum: 8

// Subtraction (-)
let difference = 5 - 3;  
console.log("Difference:", difference);  // Difference: 2

// Multiplication (*)
let product = 5 * 3;  
console.log("Product:", product);  // Product: 15

// Division (/)
let quotient = 6 / 3;  
console.log("Quotient:", quotient);  // Quotient: 2

// Modulus (%)
let remainder = 5 % 3;  
console.log("Remainder:", remainder);  // Remainder: 2

// Exponentiation (**)
let result = 2 ** 3;  
console.log("Result:", result);  // Result: 8
