// console.log()
console.log("Hello, world!");

// parseInt()
let number = parseInt("10");
console.log(number); // Output: 10

// parseFloat()
let floatNumber = parseFloat("3.14");
console.log(floatNumber); // Output: 3.14

// String.toUpperCase()
let strUpperCase = "hello".toUpperCase();
console.log(strUpperCase); // Output: HELLO

// String.toLowerCase()
let strLowerCase = "WORLD".toLowerCase();
console.log(strLowerCase); // Output: world

// Array.push()
let array = [1, 2, 3];
array.push(4);
console.log(array); // Output: [1, 2, 3, 4]

// Array.pop()
let removedElement = array.pop();
console.log(removedElement); // Output: 4

// Math.random()
let randomNum = Math.random();
console.log(randomNum);
