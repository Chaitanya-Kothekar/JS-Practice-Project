// Unary Plus (+)
let num = +"5";  
console.log("Unary Plus (+):", num);  // Output: 5

// Unary Negation (-)
let negNum = -5;  
console.log("Unary Negation (-):", negNum);  // Output: -5

// Increment (++)
let countIncrement = 5;
countIncrement++;
console.log("Increment (++):", countIncrement);  // Output: 6

// Decrement (--)
let countDecrement = 5;
countDecrement--;
console.log("Decrement (--):", countDecrement);  // Output: 4

// Logical NOT (!)
console.log("Logical NOT (!):", !true);   // Output: false

// Bitwise NOT (~)
let x = 5;
console.log("Bitwise NOT (~):", ~x);  // Output: -6

// Typeof Operator
console.log("Typeof Operator:", typeof 5);  // Output: "number"
console.log("Typeof Operator:", typeof "hello");  // Output: "string"
