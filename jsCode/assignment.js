let x = 10;
console.log("Initial value of x:", x);  // Initial value of x: 10

x += 5;  // Addition assignment operator
console.log("After adding 5, x:", x);  // After adding 5, x: 15

x -= 3;  // Subtraction assignment operator
console.log("After subtracting 3, x:", x);  // After subtracting 3, x: 12

x *= 2;  // Multiplication assignment operator
console.log("After multiplying by 2, x:", x);  // After multiplying by 2, x: 24

x /= 4;  // Division assignment operator
console.log("After dividing by 4, x:", x);  // After dividing by 4, x: 6

x %= 3;  // Modulus assignment operator
console.log("After taking modulus with 3, x:", x);  // After taking modulus with 3, x: 0

x **= 2; // Exponentiation assignment operator
console.log("After exponentiation by 2, x:", x);  // After exponentiation by 2, x: 0
